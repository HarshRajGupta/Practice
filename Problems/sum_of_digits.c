#include <stdio.h>
#include <math.h>
int main()
{
	printf("Enter a FIVE DIGIT number\n");
	int a,b,c,d,e,f,s,i,j,k,l;
	scanf("%d",&a); //a=23456

	b=a%10; //23456%10==6

	a=a-b; //23456-6=23450
	c=a%100; //23450%100==50
	l=c/10; //50/10=5

	a=a-c; //23450-50=23400
	d=a%1000; //23400%1000==400
	k=d/100; //400/100=4

	a=a-d; //23400-400=23000
	e=a%10000; //23000%10000==3000
	j=e/1000; //3000/1000=3

	a=a-e; //23000-3000=20000
	f=a%100000; //20000%100000==20000
	i=f/10000; //20000/10000=2

	s=l+k+j+i+b; //5+4+3+2+6=20

	printf("\n========================================\n");
	printf("SUM of digits = %d", s); //12505785
	printf("\n========================================\n");

	return 0;
}